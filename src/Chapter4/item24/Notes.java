package Chapter4.item24;

public class Notes {

	/*
	 * Local classes are the least frequently used of the four kinds of nested
	 * classes. A local class can be declared practically anywhere a local variable
	 * can be declared and obeys the same scoping rules. Local classes have
	 * attributes in common with each of the other kinds of nested classes. Like
	 * member classes, they have names and can be used repeatedly. Like anonymous
	 * classes, they have enclosing instances only if they are defined in a
	 * nonstatic context, and they cannot contain static members. And like anonymous
	 * classes, they should be kept short so as not to harm readability. To recap,
	 * there are four different kinds of nested classes, and each has its place. If
	 * a nested class needs to be visible outside of a single method or is too long
	 * to fit comfortably inside a method, use a member class. If each instance of a
	 * member class needs a reference to its enclosing instance, make it nonstatic;
	 * otherwise, make it static. Assuming the class belongs inside a method, if you
	 * need to create instances from only one location and there is a preexisting
	 * type that characterizes the class, make it an anonymous class; otherwise,
	 * make it a local class.
	 */

}
